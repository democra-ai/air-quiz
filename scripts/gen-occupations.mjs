/**
 * Generate the occupation anchor library from hand-authored REALISTIC answer
 * vectors (scripts/realistic-answers.mjs) — one per BLS occupation in
 * PROFILE_CAREERS — and write it into lib/occupation_anchors.generated.ts.
 *
 *   npx tsx scripts/gen-occupations.mjs
 *
 * KEY DESIGN (changed 2026-05): each anchor sits at the 16-answer vector a real
 * worker in that job would plausibly give, and its `archetype` is COMPUTED from
 * that vector via the production calculator — NOT taken from a template. This
 * makes the nearest-neighbour guesser self-consistent: an independent realistic
 * answer set for occupation O lands on O's anchor (or a true neighbour), and
 * the family label is exactly what that vector scores to.
 *
 * The script also prints a diagnostic comparing each computed archetype to the
 * PROFILE_CAREERS placement (the list the result page shows). A mismatch is not
 * a bug — it means a realistic worker scores into an adjacent family — but the
 * report helps spot vectors that drifted too far.
 */

import { writeFileSync } from 'node:fs';
import { PROFILE_CAREERS } from '../lib/air_career_data.ts';
import { calculateQuizResult } from '../lib/air_quiz_calculator.ts';
import { REALISTIC_ANSWERS, SUPPLEMENTARY_OCCUPATIONS } from './realistic-answers.mjs';

function computeArchetype(vec) {
  const core = Object.fromEntries(vec.map((a, i) => [`Q${i + 1}`, a]));
  return calculateQuizResult({ core, snapshot: {}, survey: {} }, null).profileCode;
}

function slug(title) {
  return title.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '').slice(0, 40);
}

const out = [];
const seen = new Set();
const missing = [];
const mismatches = [];

for (const [placement, careers] of Object.entries(PROFILE_CAREERS)) {
  for (const c of careers) {
    const id = slug(c.title.en);
    if (seen.has(id)) continue;
    seen.add(id);

    const answers = REALISTIC_ANSWERS[c.title.en];
    if (!answers) { missing.push(c.title.en); continue; }

    const archetype = computeArchetype(answers);
    if (archetype !== placement) mismatches.push({ title: c.title.en, placement, archetype });

    out.push({ id, title: c.title, socGroup: 0, archetype, answers });
  }
}

// Supplementary common occupations (guess-only; no PROFILE_CAREERS list).
let supCount = 0;
for (const s of SUPPLEMENTARY_OCCUPATIONS) {
  const id = slug(s.en);
  if (seen.has(id)) continue;
  seen.add(id);
  const archetype = computeArchetype(s.answers);
  out.push({ id, title: { en: s.en, zh: s.zh }, socGroup: 0, archetype, answers: s.answers });
  supCount++;
}

if (missing.length) {
  console.error(`\n✗ ${missing.length} occupations missing a realistic vector:`);
  for (const m of missing) console.error(`    ${m}`);
  process.exit(1);
}

const header = `/**
 * AUTO-GENERATED by scripts/gen-occupations.mjs — do not edit by hand.
 * ${out.length} BLS occupations (from PROFILE_CAREERS) anchored at hand-authored
 * REALISTIC 16-answer vectors. Each \`archetype\` is COMPUTED from its vector via
 * the production calculator, so the nearest-neighbour guesser is self-consistent.
 */
import type { OccupationPattern } from './occupation_inference';

export interface AnchoredOccupation extends OccupationPattern {
  archetype: string;
}

export const BLS_OCCUPATIONS: AnchoredOccupation[] = ${JSON.stringify(out, null, 2)};
`;

writeFileSync(new URL('../lib/occupation_anchors.generated.ts', import.meta.url), header);

// ── Diagnostic report ──
const byFam = {};
for (const o of out) byFam[o.archetype] = (byFam[o.archetype] || 0) + 1;
console.log(`\nwrote ${out.length} occupations (${out.length - supCount} PROFILE_CAREERS + ${supCount} supplementary) to lib/occupation_anchors.generated.ts`);
console.log(`computed-archetype distribution (${Object.keys(byFam).length}/16 families populated):`);
console.log('  ' + Object.entries(byFam).sort().map(([k, v]) => `${k}:${v}`).join('  '));
console.log(`\nplacement vs computed: ${out.length - mismatches.length}/${out.length} match PROFILE_CAREERS`);
if (mismatches.length) {
  console.log(`${mismatches.length} realistic vectors score into an adjacent family:`);
  for (const m of mismatches) {
    console.log(`    ${m.title.padEnd(52)} placed ${m.placement} → scores ${m.archetype}`);
  }
}
console.log();
